function Iteration_For() {
    var i = 0;
    for (i = 1; i <= 5; i++) {
        console.log("hello");
    }
}
function Iteration_While() {
    var i = 1;
    while (i <= 5) {
        console.log("hello");
        i++;
    }
}
Iteration_For();
Iteration_While();
