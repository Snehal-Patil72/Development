/* Algorithm

 START
    Accept first no as NO1
    Accept second no as NO2
    Perform division of NO1 &NO2
	Store the result in RET
	Return the value of RET
	STOP
	*/
	
	int Division(
	              int iNO1,     //First input argument
				  int iNO2      //Second input argument
				)
	{	
	register int iReturn=0;     //Local variable to store result
    iReturn=iNO1/iNO2;
	return iReturn;             //Return the value to caller 
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
		
		
		
	