def Addition(value1,value2):
	sum=0;
	sum=value1+value2;
	return sum;

def Multiply(value1,value2):
	mul=0;
	mul=value1*value2;
	return mul;

def Division(value1,value2):
	div=0.0;
	div=value1/value2;
	return div;

def Subtract(value1,value2):
	sub=0;
	sub=value1-value2;
	return sub;

